package VTTPmini.mini_project.model;

public class ValuationRatios {
    private double peBasic;
    private double peDiluted;
    private double priceToSales;
    private double dividendPayoutRatio;
    private double retentionRate;
    private double sustainableGrowthRate;

    public double getPeBasic() {return peBasic;}
    public void setPeBasic(double peBasic) {this.peBasic = peBasic;}
    public double getPeDiluted() {return peDiluted;}
    public void setPeDiluted(double peDiluted) {this.peDiluted = peDiluted;}
    public double getPriceToSales() {return priceToSales;}
    public void setPriceToSales(double priceToSales) {this.priceToSales = priceToSales;}
    public double getDividendPayoutRatio() {return dividendPayoutRatio;}
    public void setDividendPayoutRatio(double dividendPayoutRatio) {this.dividendPayoutRatio = dividendPayoutRatio;}
    public double getRetentionRate() {return retentionRate;}
    public void setRetentionRate(double retentionRate) {this.retentionRate = retentionRate;}
    public double getSustainableGrowthRate() {return sustainableGrowthRate;}
    public void setSustainableGrowthRate(double sustainableGrowthRate) {this.sustainableGrowthRate = sustainableGrowthRate;}
    
 

    

}
